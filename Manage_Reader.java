    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel= (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            String USERNAME= jTextField1.getText();
            String USERID= jTextField2.getText();
            String BR= jTextField3.getText();
            tblModel.setValueAt(USERNAME, jTable1.getSelectedRow(), 0);
            tblModel.setValueAt(USERID, jTable1.getSelectedRow(), 1);
            
            tblModel.setValueAt(BR, jTable1.getSelectedRow(), 2);
            JOptionPane.showMessageDialog(this, "Update Successful");
            
        }
        else{
            if(jTable1.getRowCount()==0){
             JOptionPane.showMessageDialog(this, "Table is empty");   
            } else
            {
                JOptionPane.showMessageDialog(this, "Please select single row for update");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

