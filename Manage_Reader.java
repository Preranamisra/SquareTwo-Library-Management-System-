        // TODO add your handling code here:
        DefaultTableModel tblModel= (DefaultTableModel) jTable1.getModel();
        String tblUSERNAME = tblModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblUSERID = tblModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tblBR= tblModel.getValueAt(jTable1.getSelectedRow(), 2).toString();
        jTextField1.setText(tblUSERNAME);
        jTextField2.setText(tblUSERID);
        jTextField3.setText(tblBR);
        
